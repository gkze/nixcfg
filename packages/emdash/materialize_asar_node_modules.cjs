const fs = require("fs");
const path = require("path");

const sourceNodeModules = path.join(process.cwd(), "node_modules");
const targetNodeModules = path.join(process.env.EMDASH_ASAR_DIR, "node_modules");

function packagePath(base, packageName) {
  return path.join(base, ...packageName.split("/"));
}

function packageDirs(base) {
  const dirs = [];
  for (const entry of fs.readdirSync(base, { withFileTypes: true })) {
    if (!entry.isDirectory() || entry.name.startsWith(".")) {
      continue;
    }

    const entryPath = path.join(base, entry.name);
    if (entry.name.startsWith("@")) {
      for (const scopedEntry of fs.readdirSync(entryPath, { withFileTypes: true })) {
        if (scopedEntry.isDirectory()) {
          dirs.push(path.join(entryPath, scopedEntry.name));
        }
      }
    } else {
      dirs.push(entryPath);
    }
  }
  return dirs;
}

function packageDependencies(packageJson) {
  return [
    ...Object.keys(packageJson.dependencies || {}).map((name) => ({
      name,
      optional: false,
    })),
    ...Object.keys(packageJson.optionalDependencies || {}).map((name) => ({
      name,
      optional: true,
    })),
  ];
}

function copyPackage(packageName, optional) {
  const target = packagePath(targetNodeModules, packageName);
  if (fs.existsSync(path.join(target, "package.json"))) {
    return false;
  }

  const source = packagePath(sourceNodeModules, packageName);
  if (!fs.existsSync(path.join(source, "package.json"))) {
    if (optional) {
      return false;
    }
    throw new Error("missing source package for " + packageName);
  }

  const temporaryTarget = target + ".nixcfg-copying";
  fs.rmSync(temporaryTarget, { recursive: true, force: true });
  fs.mkdirSync(path.dirname(target), { recursive: true });
  fs.cpSync(source, temporaryTarget, {
    recursive: true,
    dereference: true,
    force: true,
  });
  fs.rmSync(target, { recursive: true, force: true });
  fs.renameSync(temporaryTarget, target);
  return true;
}

let copied = 0;
let changed = true;
while (changed) {
  changed = false;
  for (const packageDir of packageDirs(targetNodeModules)) {
    const packageJsonPath = path.join(packageDir, "package.json");
    if (!fs.existsSync(packageJsonPath)) {
      continue;
    }

    const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, "utf8"));
    for (const dependency of packageDependencies(packageJson)) {
      if (copyPackage(dependency.name, dependency.optional)) {
        copied += 1;
        changed = true;
      }
    }
  }
}

console.log("materialized " + copied + " packaged node module dependencies");
