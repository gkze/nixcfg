def _update_lock_job() -> Job:
    """Phase 1: refresh flake.lock and superset lock artifacts."""
    return Job(
        comment_lines=("Phase 1: Update flake.lock",),
        runs_on="ubuntu-24.04",
        timeout_minutes=30,
        steps=(
            _checkout_step(),
            _setup_nix_step(),
            _cachix_step(
                comment_lines=(
                    "Push store paths incrementally as each build completes while also",
                    "pulling from the public zed cache.",
                ),
            ),
            _nix_cache_step(key=_hash_files_key(_UPDATE_HASH_FILES)),
            Step(
                name="Verify generated workflow files are fresh",
                run="nix run .#nixcfg -- ci workflow verify-generated",
            ),
            Step(
                name="Snapshot superset flake input",
                run=(
                    "nix run .#nixcfg -- ci workflow flake-input snapshot "
                    "--node superset --output /tmp/superset-lock-before.json"
                ),
            ),
            Step(run="nix run .#nixcfg -- ci workflow nix-flake-update"),
            Step(
                name="Detect superset flake input changes",
                id="superset-changed",
                run=(
                    "nix run .#nixcfg -- ci workflow flake-input compare "
                    "--node superset --before /tmp/superset-lock-before.json "
                    '--github-output "$GITHUB_OUTPUT"'
                ),
            ),
            Step(
                name="Regenerate superset bun2nix lock",
                if_="steps.superset-changed.outputs.changed == 'true'",
                run="nix run .#superset.passthru.updateScript",
            ),
            Step(
                name="Format regenerated superset bun.nix",
                if_="steps.superset-changed.outputs.changed == 'true'",
                run="nix fmt -- packages/superset/bun.nix",
            ),
            _upload_step(
                name="flake-lock",
                path=_lines(
                    "flake.lock",
                    "packages/superset/bun.nix",
                    "packages/superset/bun.lock",
                ),
            ),
        ),
    )


