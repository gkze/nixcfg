def _publish_pr_certification_job() -> Job:
    """Publish certification results onto the open update PR."""
    return Job(
        needs=(
            "select-ref",
            "quality-gates",
            "darwin-priority-heavy",
            "darwin-extra-heavy",
            "darwin-shared",
            "darwin-argus",
            "darwin-rocinante",
            "linux-x86_64",
        ),
        if_=f"always() && !cancelled() && {_SELECT_REF_EXISTS}",
        runs_on="ubuntu-24.04",
        timeout_minutes=20,
        permissions={
            "actions": "read",
            "contents": "read",
            "pull-requests": "write",
        },
        steps=(
            *_certify_shared_steps(),
            Step(
                name="Resolve update PR",
                id="pr",
                env={
                    "GH_TOKEN": _UPDATE_TOKEN,
                    "PR_HEAD_REF": (
                        "${{ github.repository_owner }}:"
                        "${{ needs.select-ref.outputs.ref }}"
                    ),
                },
                run=_lines(
                    'pr_number="$(gh pr list \\',
                    '  --repo "${GITHUB_REPOSITORY}" \\',
                    '  --head "${PR_HEAD_REF}" \\',
                    "  --state open \\",
                    "  --json number \\",
                    '  --jq \'.[0].number // ""\')"',
                    'if [ -z "${pr_number}" ]; then',
                    '  echo "found=false" >> "$GITHUB_OUTPUT"',
                    "  echo \"No open PR found for ref '${PR_HEAD_REF}'; "
                    'skipping update." >&2',
                    "  exit 0",
                    "fi",
                    'echo "found=true" >> "$GITHUB_OUTPUT"',
                    'echo "number=${pr_number}" >> "$GITHUB_OUTPUT"',
                ),
            ),
            Step(
                name="Capture current PR body",
                if_="steps.pr.outputs.found == 'true'",
                env={"GH_TOKEN": _UPDATE_TOKEN},
                run=(
                    'gh pr view "${{ steps.pr.outputs.number }}" '
                    '--repo "${{ github.repository }}" --json body '
                    "--template '{{.body}}' > /tmp/pr-body-current.md"
                ),
            ),
            Step(
                name="Capture certification workflow run metadata",
                if_="steps.pr.outputs.found == 'true'",
                env={"GH_TOKEN": _UPDATE_TOKEN},
                run=(
                    "gh api repos/${{ github.repository }}/actions/runs/"
                    "${{ github.run_id }} > /tmp/certification-run.json"
                ),
            ),
            Step(
                name="Capture certification job results",
                if_="steps.pr.outputs.found == 'true'",
                env={"GH_TOKEN": _UPDATE_TOKEN},
                run=(
                    'gh api "repos/${{ github.repository }}/actions/runs/'
                    '${{ github.run_id }}/jobs?per_page=100" '
                    "> /tmp/certification-jobs.json"
                ),
            ),
            Step(
                name="Render updated PR body",
                if_="steps.pr.outputs.found == 'true'",
                run=(
                    "nix run .#nixcfg -- ci workflow "
                    "render-certification-pr-body "
                    "--existing-body /tmp/pr-body-current.md "
                    "--output /tmp/pr-body-updated.md "
                    "--run-json /tmp/certification-run.json "
                    "--jobs-json /tmp/certification-jobs.json "
                    '--cachix-name "${{ env.CACHIX_NAME }}"'
                ),
            ),
            Step(
                name="Update PR body with certification details",
                if_="steps.pr.outputs.found == 'true'",
                env={"GH_TOKEN": _UPDATE_TOKEN},
                run=(
                    'gh pr edit "${{ steps.pr.outputs.number }}" '
                    '--repo "${{ github.repository }}" '
                    "--body-file /tmp/pr-body-updated.md"
                ),
            ),
        ),
    )


