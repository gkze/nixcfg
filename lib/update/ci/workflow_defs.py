def _tarball_checkout_step(
    *, step_name: str, ref_expression: str, env: dict[str, str]
) -> Step:
    """Build the tarball-based checkout used on hosted arm64 Linux runners."""
    return Step(
        name=step_name,
        env=env,
        run=_lines(
            "set -euo pipefail",
            'tmp_dir="$(mktemp -d)"',
            "trap 'rm -rf \"$tmp_dir\"' EXIT",
            "curl --fail --location --location-trusted --silent --show-error \\",
            '  --header "Authorization: Bearer ${GITHUB_TOKEN}" \\',
            '  --header "X-GitHub-Api-Version: 2022-11-28" \\',
            '  --output "$tmp_dir/repo.tar.gz" \\',
            '  "${GITHUB_API_URL:-https://api.github.com}/repos/'
            "${GITHUB_REPOSITORY}/tarball/" + ref_expression + '"',
            'tar -xzf "$tmp_dir/repo.tar.gz" --strip-components=1',
            "git init -q",
            "git config user.name github-actions",
            "git config user.email github-actions@github.com",
            "git add -A",
            'git commit -q -m "workspace snapshot"',
        ),
    )


