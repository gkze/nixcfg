def _hash_files_key(files: tuple[str, ...]) -> str:
    """Build one tarball-cache key over hashed lockfile inputs."""
    quoted = ", ".join(f"'{name}'" for name in files)
    return (
        "nix-cache-${{ runner.os }}-${{ runner.arch }}-"
        "${{ hashFiles( " + quoted + " ) }}"
    )


