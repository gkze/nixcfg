def _checkout_step(
    *,
    fetch_depth: int | None = None,
    token: str | None = _UPDATE_TOKEN,
    ref: str | None = None,
) -> Step:
    """Build one actions/checkout step."""
    with_: dict[str, str | int | bool] = {}
    if fetch_depth is not None:
        with_["fetch-depth"] = fetch_depth
    with_["persist-credentials"] = False
    if token is not None:
        with_["token"] = token
    if ref is not None:
        with_["ref"] = ref
    return Step(
        uses=_CHECKOUT_USES,
        uses_comment=_CHECKOUT_VERSION,
        with_=with_,
    )


def _setup_nix_step() -> Step:
    """Build the pinned Determinate Nix installer step."""
    return Step(
        comment_lines=(
            "Determinate Nix v3.17.1",
            "Nix uses this token for external api.github.com flake fetches.",
            'The default github.token produces HTTP 401 "Bad credentials" there.',
        ),
        uses=_SETUP_NIX_USES,
        with_={"github-token": _UPDATE_TOKEN},
    )


def _cachix_step(
    *,
    if_: str | None = None,
    push: bool = True,
    comment_lines: tuple[str, ...] = (),
) -> Step:
    """Build one cachix/cachix-action step."""
    with_: dict[str, str | int | bool] = {"name": "${{ env.CACHIX_NAME }}"}
    if push:
        with_["authToken"] = "${{ secrets.CACHIX_AUTH_TOKEN }}"
    with_["extraPullNames"] = "zed"
    if push:
        with_["useDaemon"] = True
    else:
        with_["skipPush"] = True
    return Step(
        comment_lines=comment_lines,
        if_=if_,
        uses=_CACHIX_USES,
        uses_comment=_CACHIX_VERSION,
        with_=with_,
    )


def _nix_cache_step(
    *,
    key: str,
    comment_lines: tuple[str, ...] = _NIX_CACHE_COMMENTS,
) -> Step:
    """Build one tarball-cache actions/cache step."""
    return Step(
        comment_lines=comment_lines,
        uses=_CACHE_USES,
        uses_comment=_CACHE_VERSION,
        with_={
            "path": _NIX_CACHE_PATH,
            "key": key,
            "restore-keys": _NIX_CACHE_RESTORE_KEYS,
        },
    )


def _upload_step(
    *,
    name: str,
    path: str,
    if_no_files_found: str = "error",
    if_: str | None = None,
) -> Step:
    """Build one actions/upload-artifact step."""
    return Step(
        if_=if_,
        uses=_UPLOAD_USES,
        uses_comment=_UPLOAD_VERSION,
        with_={"name": name, "if-no-files-found": if_no_files_found, "path": path},
    )


def _download_step(
    *,
    name: str | None = None,
    pattern: str | None = None,
    path: str,
    step_name: str | None = None,
    step_id: str | None = None,
    if_: str | None = None,
    continue_on_error: bool | None = None,
    comment_lines: tuple[str, ...] = (),
) -> Step:
    """Build one actions/download-artifact step."""
    with_: dict[str, str | int | bool] = {}
    if name is not None:
        with_["name"] = name
    if pattern is not None:
        with_["pattern"] = pattern
    with_["path"] = path
    return Step(
        comment_lines=comment_lines,
        name=step_name,
        id=step_id,
        if_=if_,
        continue_on_error=continue_on_error,
        uses=_DOWNLOAD_USES,
        uses_comment=_DOWNLOAD_VERSION,
        with_=with_,
    )


